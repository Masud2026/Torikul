let allChannels = [];
let favoriteChannels = JSON.parse(localStorage.getItem('iptv_favs')) || [];
let currentCategory = 'all';

window.addEventListener('DOMContentLoaded', () => {
    parseM3U(m3uData);
    generateBackupChannels(); // ১০০টি চ্যানেল পূর্ণ করার ব্যাকআপ জেনারেটর
    renderCategoryTabs();
    displayChannels(allChannels);

    document.getElementById('search-input').addEventListener('input', filterChannels);
});

function parseM3U(data) {
    const lines = data.split('\n');
    let currentChannel = {};

    lines.forEach(line => {
        line = line.trim();
        if (line.startsWith('#EXTINF:')) {
            currentChannel = {};
            const groupMatch = line.match(/group-title="([^"]+)"/);
            currentChannel.group = groupMatch ? groupMatch[1] : "Others";

            const logoMatch = line.match(/tvg-logo="([^"]+)"/);
            currentChannel.logo = logoMatch ? logoMatch[1] : "";

            const commaIndex = line.lastIndexOf(',');
            if (commaIndex !== -1) {
                let name = line.substring(commaIndex + 1).trim();
                currentChannel.name = name.replace(/^Torikul\s*\|\s*/i, '');
            } else {
                currentChannel.name = 'Live Channel';
            }
        } 
        else if (line.startsWith('http://') || line.startsWith('https://')) {
            if (currentChannel.name) {
                currentChannel.url = line;
                allChannels.push(currentChannel);
                currentChannel = {}; 
            }
        }
    });
}

function generateBackupChannels() {
    const categories = ["Bangla", "News", "Sports", "Movies", "Islamic", "Infotainment"];
    let currentCount = allChannels.length;
    let targetCount = 100;
    
    for (let i = currentCount + 1; i <= targetCount; i++) {
        let randomCat = categories[i % categories.length];
        allChannels.push({
            name: `Premium Extra Ch-${i}`,
            group: randomCat,
            logo: `https://via.placeholder.com/150/ffffff/000000?text=CH-${i}`,
            url: "https://owrcovcrpy.gpcdn.net/bpk-tv/1715/output/index.m3u8"
        });
    }
}

function renderCategoryTabs() {
    const container = document.getElementById('category-tabs-container');
    const categories = [...new Set(allChannels.map(ch => ch.group))];

    categories.forEach(cat => {
        if(cat === "Others") return;
        const btn = document.createElement('button');
        btn.className = 'tab-btn';
        btn.setAttribute('data-category', cat);
        btn.textContent = cat;
        
        btn.addEventListener('click', (e) => {
            document.querySelectorAll('.tab-btn').forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            currentCategory = cat;
            filterChannels();
        });
        container.appendChild(btn);
    });

    document.querySelector('[data-category="all"]').addEventListener('click', (e) => {
        document.querySelectorAll('.tab-btn').forEach(b => b.classList.remove('active'));
        e.target.classList.add('active');
        currentCategory = 'all';
        filterChannels();
    });

    document.querySelector('[data-category="fav"]').addEventListener('click', (e) => {
        document.querySelectorAll('.tab-btn').forEach(b => b.classList.remove('active'));
        document.querySelector('[data-category="fav"]').classList.add('active');
        currentCategory = 'fav';
        filterChannels();
    });
}

function filterChannels() {
    const searchText = document.getElementById('search-input').value.toLowerCase();
    let filtered = allChannels;

    if (currentCategory === 'fav') {
        filtered = allChannels.filter(ch => favoriteChannels.includes(ch.url));
    } else if (currentCategory !== 'all') {
        filtered = allChannels.filter(ch => ch.group === currentCategory);
    }

    if (searchText) {
        filtered = filtered.filter(ch => ch.name.toLowerCase().includes(searchText));
    }

    displayChannels(filtered);
}

function displayChannels(channels) {
    const grid = document.getElementById('channel-grid');
    grid.innerHTML = '';

    if (channels.length === 0) {
        grid.innerHTML = '<p style="grid-column: 1/-1; text-align:center; padding:20px; color:#666;">No channels found!</p>';
        return;
    }

    channels.forEach(ch => {
        const card = document.createElement('div');
        card.className = 'channel-card';
        const isFav = favoriteChannels.includes(ch.url) ? 'liked' : '';

        card.innerHTML = `
            <i class="fas fa-heart fav-icon ${isFav}"></i>
            <div class="logo-wrapper">
                <img src="${ch.logo}" alt="${ch.name}" onerror="this.src='https://via.placeholder.com/150/ffffff/000000?text=TV'">
            </div>
            <div class="channel-name-box">${ch.name}</div>
        `;

        card.querySelector('.fav-icon').addEventListener('click', (e) => {
            e.stopPropagation();
            toggleFavorite(ch.url, e.target);
        });

        card.addEventListener('click', () => {
            playVideo(ch.url, ch.name);
            window.scrollTo({ top: 0, behavior: 'smooth' });
        });

        grid.appendChild(card);
    });
}

function toggleFavorite(url, element) {
    if (favoriteChannels.includes(url)) {
        favoriteChannels = favoriteChannels.filter(id => id !== url);
        element.classList.remove('liked');
    } else {
        favoriteChannels.push(url);
        element.classList.add('liked');
    }
    localStorage.setItem('iptv_favs', JSON.stringify(favoriteChannels));
    if(currentCategory === 'fav') filterChannels();
}

function playVideo(url, name) {
    const video = document.getElementById('video-player');
    document.getElementById('current-channel-title').textContent = "এখন দেখছেন: " + name;

    if (Hls.isSupported()) {
        const hls = new Hls();
        hls.loadSource(url);
        hls.attachMedia(video);
        hls.on(Hls.Events.MANIFEST_PARSED, () => {
            video.play();
        });
    } else if (video.canPlayType('application/vnd.apple.mpegurl')) {
        video.src = url;
        video.addEventListener('loadedmetadata', () => {
            video.play();
        });
    }
}
